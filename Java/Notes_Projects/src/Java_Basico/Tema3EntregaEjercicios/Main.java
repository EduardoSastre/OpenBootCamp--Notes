package Java_Basico.Tema3EntregaEjercicios;

public class Main {
    public static void main(String[] args) {
        String[] nombres = {"Juan", "Mario", "Ara", "Karen"};
        String[] apellidos = {"Ramos", "Bros", "Luna", "Ramirez"};

        for (int i = 0; i < nombres.length; i++) {
            System.out.println(nombres[i] + " " + apellidos[i]);
        }
    }
}
